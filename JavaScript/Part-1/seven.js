// 1. AND (&) Operator:
let a = 10; // binary: 1010
let b = 6; // binary: 0110
let result = a & b; // binary: 0010 (decimal: 2)

// 2. OR (|) operator
let a2 = 10; // binary: 1010
let b2 = 6; // binary: 0110
let result2 = a2 | b2; // binary: 1110 (decimal: 14)

// 3. XOR (^) operator:
let a3 = 10; // binary: 1010
let b3 = 6; // binary: 0110
let result3 = a3 ^ b3; // binary: 1100 (decimal: 12)

// 4. NOT (~) operator:
let a4 = 10; // binary: 00000...1010(32 bits)
let result4 = ~a4; // binary: 11111...0101(32 bits) (decimal: -11)

// 5. Left shift (<<) operator:
let a5 = 5; // binary: 0101
let result5 = a5 << 2; // binary: 010100 (decimal: 20)

// 6. Right shift (>>) operator:
let a6 = 20; // binary: 010100
let result6 = a6 >> 2; // binary: 0101 (decimal: 5)